						<div class="entry-excerpt">Cựu golf thủ người Mỹ ra đi ở bệnh viện Presbyterian ở Pittsburgh, Pennsylvania, nơi ông điều trị bệnh tim.</div>
						<div class="entry-content">
							<div class="wp-caption aligncenter" style="width:500px">
								<img src="images/news-1.jpg" alt="">
								<p class="wp-caption-text">Arnold Palmer được xem làm một trong những golf thủ hay nhất mọi thời đại, và có công lớn trong việc đưa môn thể thao này phát triển trên toàn thế giới. Ảnh: EPA.</p>
							</div>
							<p>Trong suốt sự nghiệp, Arnold Palmer chiến thắng khoảng 90 giải đấu trên toàn thế giới, trong đó có bảy danh hiệu major. Bình luận viên Iain Carter của BBC gọi ông là "golf thủ quan trọng nhất từng thi đấu". Theo Carter, uy tín của Palmer đã đưa môn golf lan tỏa khắp thế giới và không bao giờ bị phai mờ.</p>
							<p>Một huyền thoại khác và là bạn của Palmer, Jack Nicklaus, nói: "Chúng ta vừa mất một trong những cá nhân phi thường nhất của môn golf nói riêng và thể thao nói chung. Arnold siêu việt trong golf . Tầm vóc của ông vượt hơn cả một golf thủ, thậm chí hơn cả một golf thủ vĩ đại. Ông ấy là biểu tượng. Một huyền thoại".</p>
							<div class="wp-caption aligncenter" style="width:500px">
								<img src="images/news-10.jpg" alt="">
								<p class="wp-caption-text"> Bức ảnh được Tổng thống Obama đăng trên Twitter, Palmer đứng thứ ba từ trái sang. Ảnh: Twitter.</p>
							</div>
							<p>Palmer có hàng nghìn người hâm mộ nhiệt thành, tụ họp lại với tên gọi "binh đoàn của Arnie". Trong thời đại nở rộ của truyền hình, ông góp công lớn trong việc đưa môn thể thao này phủ sóng rộng khắp.</p>
							<p>Tổng thống Mỹ Barack Obama đăng một bức ảnh khi Palmer đến thăm Nhà trắng cùng lời vinh danh sau khi ông qua đời: "Đây là một vị vua, người phi thường trên sân golf và rộng lượng với người khác. Cảm ơn về những kỷ niệm Arnold".</p>
